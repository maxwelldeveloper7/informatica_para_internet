def soma():
    n1 = int(input('Informe o primeiro número: '))
    n2 = int(input('Informe o segundo número: '))
    resultado = n1 + n2
    print(f'O resultado da soma de {n1} + {n2} = {resultado}')

def subtrai():
    n1 = int(input('Informe o primeiro número: '))
    n2 = int(input('Informe o segundo número: '))
    resultado = n1 - n2
    print(f'O resultado da subtração de {n1} - {n2} = {resultado}')

def multiplica():
    n1 = int(input('Informe o primeiro número: '))
    n2 = int(input('Informe o segundo número: '))
    resultado = n1 * n2
    print(f'O resultado da multiplicação de {n1} x {n2} = {resultado}')

def divide():
    n1 = int(input('Informe o primeiro número: '))
    n2 = int(input('Informe o segundo número: '))
    resultado = n1 / n2
    print(f'O resultado da divisão de {n1} / {n2} = {resultado}')